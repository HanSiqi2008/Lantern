package org.lanternpowered.server.network.vanilla.message.codec

import org.lanternpowered.server.network.buffer.ByteBuffer
import org.lanternpowered.server.network.buffer.contextual.ContextualValueTypes
import org.lanternpowered.server.network.message.codec.Codec
import org.lanternpowered.server.network.message.codec.CodecContext
import org.lanternpowered.server.network.vanilla.message.type.DisconnectMessage

class DisconnectCodec : Codec<DisconnectMessage> {

    override fun encode(context: CodecContext, message: DisconnectMessage): ByteBuffer {
        val buf = context.byteBufAlloc().buffer()
        context.write(buf, ContextualValueTypes.TEXT, message.reason)
        return buf
    }
}
